/* ===================================================
   RENDER ALL
   =================================================== */
function renderAll() {
  renderPodium();
  renderTable();
  renderBreakdown();
  populateH2H();
  renderH2H();
}

/* ===================================================
   PODIUM — Top 3
   =================================================== */
function renderPodium() {
  const podium = document.getElementById("podium");
  podium.innerHTML = "";
  const top = allStats.slice(0, 3);
  if (!top.length) { podium.innerHTML = `<p class="empty-state">No data — run fetch_data.py first.</p>`; return; }

  const rankClass = ["rank-1", "rank-2", "rank-3"];
  const rankEmoji = ["🥇", "🥈", "🥉"];

  top.forEach((user, i) => {
    const card = document.createElement("div");
    card.className = `podium-card ${rankClass[i]}`;

    const extra = [];
    if (user.contestRating) extra.push(`<span style="color:#ffa836">⚡ ${user.contestRating} Rating</span>`);
    if (user.streak)        extra.push(`<span style="color:#00ff88">🔥 ${user.streak} day streak</span>`);

    card.innerHTML = `
      <div class="podium-rank">${rankEmoji[i]}</div>
      <div class="podium-username">${user.username}</div>
      ${user.realName ? `<div style="font-size:0.65rem;color:#586374;letter-spacing:1px;margin-bottom:0.4rem">${user.realName}</div>` : ""}
      <div class="podium-total">${user.totalSolved}</div>
      <div class="podium-total-label">PROBLEMS SOLVED</div>
      <div class="podium-badges">
        <span class="badge badge-e">E: ${user.easySolved}</span>
        <span class="badge badge-m">M: ${user.mediumSolved}</span>
        <span class="badge badge-h">H: ${user.hardSolved}</span>
      </div>
      <div style="font-size:0.65rem;margin-top:0.6rem;display:flex;flex-direction:column;gap:0.2rem;align-items:center">
        ${extra.join("")}
      </div>
    `;
    podium.appendChild(card);
  });
}

/* ===================================================
   RANK TABLE
   =================================================== */
function renderTable() {
  const tbody = document.getElementById("rankBody");
  tbody.innerHTML = "";
  if (!allStats.length) return;

  // Update header to show Contest Rating instead of Streak
  document.querySelector("#rankTable thead tr").innerHTML = `
    <th>#</th><th>USERNAME</th><th>TOTAL</th>
    <th>EASY</th><th>MEDIUM</th><th>HARD</th>
    <th>ACCEPT %</th><th>LC RANK</th><th>🔥 STREAK</th><th>⚡ CONTEST</th>
  `;

  allStats.forEach((user, i) => {
    const rank = i + 1;
    const rankCls = rank <= 3 ? `r${rank}` : "";
    const acc = user.acceptanceRate ? `${user.acceptanceRate}%` : "—";
    const lcRank = user.ranking ? `#${parseInt(user.ranking).toLocaleString()}` : "—";
    const initials = user.username.slice(0, 2).toUpperCase();
    const streak = user.streak ? `${user.streak}d` : "—";
    const rating = user.contestRating || "—";

    const tr = document.createElement("tr");
    tr.innerHTML = `
      <td><span class="rank-cell ${rankCls}">${rank}</span></td>
      <td>
        <div class="user-cell">
          <div class="user-avatar">${user.avatar
            ? `<img src="${user.avatar}" style="width:100%;height:100%;border-radius:50%;object-fit:cover">`
            : initials}</div>
          <a class="user-link" href="https://leetcode.com/u/${user.username}/" target="_blank">${user.username}</a>
        </div>
      </td>
      <td class="total-cell">${user.totalSolved}</td>
      <td class="easy-cell">${user.easySolved}</td>
      <td class="medium-cell">${user.mediumSolved}</td>
      <td class="hard-cell">${user.hardSolved}</td>
      <td class="acc-cell">${acc}</td>
      <td class="ranking-cell">${lcRank}</td>
      <td style="color:#00ff88;font-weight:600">${streak}</td>
      <td style="color:#ffa836;font-weight:600">${rating}</td>
    `;
    tbody.appendChild(tr);
  });
}

/* ===================================================
   DIFFICULTY BREAKDOWN
   =================================================== */
function renderBreakdown() {
  const grid = document.getElementById("breakdownGrid");
  grid.innerHTML = "";
  if (!allStats.length) return;

  allStats.forEach(user => {
    const easyPct  = Math.min((user.easySolved   / Math.max(user.totalEasy, 1))   * 100, 100);
    const medPct   = Math.min((user.mediumSolved  / Math.max(user.totalMedium, 1)) * 100, 100);
    const hardPct  = Math.min((user.hardSolved    / Math.max(user.totalHard, 1))   * 100, 100);

    const card = document.createElement("div");
    card.className = "breakdown-card";

    const extras = [];
    if (user.totalActiveDays) extras.push(`<span style="color:#586374;font-size:0.65rem">${user.totalActiveDays} active days</span>`);
    if (user.contestsAttended) extras.push(`<span style="color:#586374;font-size:0.65rem">${user.contestsAttended} contests</span>`);

    card.innerHTML = `
      <div class="bc-header">
        <span class="bc-username">${user.username}</span>
        <span class="bc-total">${user.totalSolved}</span>
      </div>
      <div class="bc-bar-row">
        <span class="bc-label bc-easy">EASY</span>
        <div class="bc-bar-bg"><div class="bc-bar-fill easy" data-pct="${easyPct.toFixed(1)}"></div></div>
        <span class="bc-count">${user.easySolved}/${user.totalEasy}</span>
      </div>
      <div class="bc-bar-row">
        <span class="bc-label bc-medium">MED</span>
        <div class="bc-bar-bg"><div class="bc-bar-fill medium" data-pct="${medPct.toFixed(1)}"></div></div>
        <span class="bc-count">${user.mediumSolved}/${user.totalMedium}</span>
      </div>
      <div class="bc-bar-row">
        <span class="bc-label bc-hard">HARD</span>
        <div class="bc-bar-bg"><div class="bc-bar-fill hard" data-pct="${hardPct.toFixed(1)}"></div></div>
        <span class="bc-count">${user.hardSolved}/${user.totalHard}</span>
      </div>
      <div style="display:flex;gap:1rem;margin-top:0.6rem">${extras.join("")}</div>
    `;
    grid.appendChild(card);
  });

  requestAnimationFrame(() => {
    document.querySelectorAll(".bc-bar-fill").forEach(b => b.style.width = b.dataset.pct + "%");
  });
}

/* ===================================================
   HEAD TO HEAD
   =================================================== */
function populateH2H() {
  const left = document.getElementById("h2hLeft");
  const right = document.getElementById("h2hRight");
  const savedL = left.value, savedR = right.value;
  left.innerHTML = right.innerHTML = "";
  allStats.forEach(u => {
    left.innerHTML  += `<option value="${u.username}">${u.username}</option>`;
    right.innerHTML += `<option value="${u.username}">${u.username}</option>`;
  });
  if (allStats.length >= 2) {
    left.value  = allStats.find(u => u.username === savedL) ? savedL : allStats[0].username;
    right.value = allStats.find(u => u.username === savedR) ? savedR : allStats[1].username;
  }
}

function renderH2H() {
  const L = allStats.find(u => u.username === document.getElementById("h2hLeft").value);
  const R = allStats.find(u => u.username === document.getElementById("h2hRight").value);
  const display = document.getElementById("h2hDisplay");

  if (!L || !R || L.username === R.username) {
    display.innerHTML = `<p class="empty-state">Pick two different players.</p>`;
    return;
  }

  const cats = [
    { label: "TOTAL SOLVED",   lv: L.totalSolved,    rv: R.totalSolved },
    { label: "EASY",           lv: L.easySolved,     rv: R.easySolved },
    { label: "MEDIUM",         lv: L.mediumSolved,   rv: R.mediumSolved },
    { label: "HARD",           lv: L.hardSolved,     rv: R.hardSolved },
    { label: "ACCEPT %",       lv: L.acceptanceRate||0, rv: R.acceptanceRate||0, fmt: v => v + "%" },
    { label: "CONTEST RTG",    lv: L.contestRating||0,  rv: R.contestRating||0 },
    { label: "STREAK (days)",  lv: L.streak||0,      rv: R.streak||0 },
    { label: "ACTIVE DAYS",    lv: L.totalActiveDays||0, rv: R.totalActiveDays||0 },
  ];

  let lW = 0, rW = 0;
  cats.forEach(c => { if (c.lv > c.rv) lW++; else if (c.rv > c.lv) rW++; });
  const winner = lW > rW ? L.username : rW > lW ? R.username : null;

  const rows = cats.map(c => {
    const fmt = c.fmt || (v => v || 0);
    const lWin = c.lv > c.rv, rWin = c.rv > c.lv;
    return `<div class="h2h-row">
      <span class="lval ${lWin?"winner":rWin?"loser":""}">${fmt(c.lv)}</span>
      <span class="cat">${c.label}</span>
      <span class="rval ${rWin?"winner":lWin?"loser":""}">${fmt(c.rv)}</span>
    </div>`;
  }).join("");

  const banner = winner
    ? `<div class="h2h-winner-banner"><span>🏆 ${winner} WINS THE BATTLE</span></div>`
    : `<div class="h2h-winner-banner"><span>🤝 IT'S A TIE</span></div>`;

  display.innerHTML = `
    <div class="h2h-grid">
      <div class="h2h-player h2h-left">
        <div class="h2h-name">${L.username}</div>
        <div style="font-family:var(--font-head);font-size:3rem;color:var(--accent2)">${L.totalSolved}</div>
        <div style="font-size:0.6rem;color:var(--text-dim);letter-spacing:2px">SOLVED</div>
      </div>
      <div class="h2h-divider"><div class="h2h-stats-grid">${rows}</div></div>
      <div class="h2h-player h2h-right">
        <div class="h2h-name">${R.username}</div>
        <div style="font-family:var(--font-head);font-size:3rem;color:var(--accent3)">${R.totalSolved}</div>
        <div style="font-size:0.6rem;color:var(--text-dim);letter-spacing:2px">SOLVED</div>
      </div>
    </div>
    ${banner}
  `;
}

/* ===================================================
   UI HELPERS
   =================================================== */
function showLoading(show) {
  document.getElementById("loadingOverlay").classList.toggle("hidden", !show);
}
function showError(msg) {
  const t = document.getElementById("errorToast");
  t.textContent = msg; t.classList.remove("hidden");
  setTimeout(() => t.classList.add("hidden"), 10000);
}

