/* ===================================================
   LEETCODE BATTLE BOARD — script.js
   Reads from data.json (generated by fetch_data.py)
   No API calls. No CORS. No setup needed.
   =================================================== */

let allStats = [];

/* ===================================================
   LOAD data.json
   =================================================== */
async function loadData() {
  showLoading(true);
  try {
    const res = await fetch("data.json?t=" + Date.now()); // cache-bust
    if (!res.ok) throw new Error("data.json not found");
    const json = await res.json();

    allStats = (json.users || []).sort((a, b) => b.totalSolved - a.totalSolved);

    document.getElementById("lastUpdated").textContent =
      "DATA FROM " + new Date(json.updatedAt).toLocaleString([], {
        month: "short", day: "numeric", hour: "2-digit", minute: "2-digit"
      }).toUpperCase();

    renderAll();
  } catch (e) {
    showError("⚠ Could not load data.json — run fetch_data.py first, then push to GitHub.");
    showSetupBanner();
  } finally {
    showLoading(false);
  }
}

